#include "get_next_line.h"

int	main(void)
{
	int		fd;
	char	*line;

	fd = open("famous_song_of_chilldhood.txt", O_RDONLY);
	while (get_next_line(fd, &line) > 0)
	{
		printf("%s\n", line);
		free(line);
	}
	close(fd);
}
